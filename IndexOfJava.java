public class IndexOfJava{
  public static void main(String[] args){

    // Declaring Word
    String word = "abcdefghijklmnopqrstuvwxyz";

    
    // ------------- Index -------------
    
    System.out.println(word.indexOf("cd"));
    // 2
    
    System.out.println(word.indexOf("az"));
    // -1
  }
}
